def area_A (r:float) -> float:
    # Calcula el área de un círculo cuando resibe su radio.
    areat = 2*r/ 3.14159265359
    return areat

def area_b (c:float) -> float:
    #Calcula el área de un cuadrado cuando resibe el lado.
    areat = c**2
    return areat 

def area_c (base:float, altura:float) -> float:
    #Calcula el área de un triángulo cuando resibe su base y altura.
    areat = (base * altura)/2
    return areat
    




