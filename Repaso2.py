def perimetro_A (r:float) -> float:
    #Calcula el perímetro de un círculo cuando resibe su radio.
    perimetrot = 2 * 3.14159265359 * r
    return perimetrot

def perimetro_b (c:float) -> float:
    #Calcula el perímetro de un cuadrado cuando resibe el lado.
    perimetrot = c * 4
    return perimetrot

def perimetro_c (base:float, altura:float) -> float:
     # Calcula el perímetro de un triángulo cuando resibe base y altura 
    perimetrot = ((base * 2 + altura * 2)/2)
    return perimetrot
    




